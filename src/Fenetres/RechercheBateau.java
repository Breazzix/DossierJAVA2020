    private Bateau searchBatAmarre(Vector<Ponton> list, String nomBat) {
        Enumeration enm = list.elements();
        Bateau bat;
        ArrayList<BateauAmarre> listBateaux = new ArrayList<BateauAmarre>();
        
        int a = 0;
        for (int i = 0; enm.hasMoreElements(); i++) {
            Ponton unPonton = (Ponton) enm.nextElement();
            for (int j = 0; j < 2; j++) {
                for (int k = 0; k < unPonton.getNombreEmplacements(); k++) {
                    bat = (Bateau) unPonton.getListe(j + 1)[k];
                    
                    System.out.println("bat = " + bat + "    nomBat = " + nomBat);
                    
                    if (bat != null)
                        if (bat.getNom().equals(nomBat))
                            return bat;
                }
            }
        }
        return null;
    }
    
    private void AfficheBateau(Bateau bat) {
        jLblLongueur.setText(String.valueOf(bat.getLongueur()) );
        jLblPavillon.setText(bat.getPavillon());
        
        
        String pattern = "MM/dd/yyyy HH:mm:ss";
        DateFormat df = new SimpleDateFormat(pattern);
        String dateAsString = df.format(bat.getDateArriver());
        
        
        jLblDate.setText(dateAsString);
        
        AfficheEquip(bat.getEquipage());
        
        FichierLog.Writer("RechercheBateau", "Affichage de bateau");
    }
    
    private void AfficheEquip (Equipage equip) {
        DefaultListModel dlm = new DefaultListModel<>();
        
        if (equip.getCapitaine().getNom() != "inconnu") 
            dlm.addElement(equip.getCapitaine());
        
        if (equip.getSecond().getNom() != "inconnu") 
            dlm.addElement(equip.getSecond());
        
        for (Marin m : equip.getLiMarins()) {
            dlm.addElement(m);
        }
        
        jListEquipage.setModel(dlm);
        
        FichierLog.Writer("RechercheBateau", "Affichage d'equipe");
        
    }
    

